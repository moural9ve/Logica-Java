public class ExpressoesComparativas {
    public static void main(String[] args) {

        int x = 10;
        int y = 20;

        //x maior que y?
        System.out.println(x>y);
        //x menor que y?
        System.out.println(x<y);
        //x maior ou igual y?
        System.out.println(x>=y);
        //x menor ou igual y?
        System.out.println(x<=y);
        //x igual y?
        System.out.println(x==y);
        //x diferente de y?
        System.out.println(x!=y);

    }
}
